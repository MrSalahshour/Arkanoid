package ir.sharif.math.ap99_2.arkanoid.models;

public class BallPosition {
    private double x,y;

    public BallPosition(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
